from pathlib import Path
from statistics import mean

from evals.runner import run_all_scenarios

RESULTS_PATH = Path("results/agent_eval.md")
MAX_FAILURE_CASES = 15


def _rate(rows: list[dict], key: str) -> float:
    values = [row[key] for row in rows if row[key] is not None]
    return mean(values) if values else float("nan")


def build_report(rows: list[dict]) -> str:
    harness_rows = [row for row in rows if row.get("harness_error")]
    agent_rows = [row for row in rows if not row.get("harness_error")]

    overall_success = _rate(agent_rows, "task_success")
    tool_accuracy = _rate(agent_rows, "tool_call_accuracy")
    escalation_correctness = _rate(agent_rows, "escalation_correct")
    avg_turns = _rate(agent_rows, "turns_to_resolution")
    judge_scores = [r["judge_score"] for r in agent_rows if r["judge_score"] is not None]
    avg_judge = mean(judge_scores) if judge_scores else float("nan")
    kb_rows = [r for r in agent_rows if r["category"] == "kb_question"]
    citation_rate = _rate(kb_rows, "citation_present") if kb_rows else float("nan")

    categories = sorted({row["category"] for row in agent_rows})
    category_lines = []
    for category in categories:
        subset = [row for row in agent_rows if row["category"] == category]
        category_lines.append(
            f"| {category} | {len(subset)} | {_rate(subset, 'task_success'):.0%} | {_rate(subset, 'tool_call_accuracy'):.0%} |"
        )

    detail_lines = []
    for row in rows:
        if row.get("harness_error"):
            detail_lines.append(f"| {row['scenario_id']} | {row['category']} | ⚠️ harness error | n/a | n/a |")
            continue
        judge = f"{row['judge_score']}/5" if row["judge_score"] is not None else "n/a"
        detail_lines.append(
            f"| {row['scenario_id']} | {row['category']} | {'✅' if row['task_success'] else '❌'} | "
            f"{row['turns_to_resolution']} | {judge} |"
        )

    failures = [row for row in agent_rows if not row["task_success"]]
    failure_lines = []
    for row in failures[:MAX_FAILURE_CASES]:
        reply_excerpt = row["final_reply"].replace("\n", " ")[:200]
        failure_lines.append(
            f"- **{row['scenario_id']}** ({row['category']}) — goal: \"{row['goal']}\"\n"
            f"  - Final reply: \"{reply_excerpt}\"\n"
            f"  - Judge note: {row['judge_justification']}"
        )

    failure_note = (
        (
            f"{len(failures)} of {len(agent_rows)} genuinely-run scenarios failed their task-success check; "
            f"showing all {len(failures)} below."
            if len(failures) <= MAX_FAILURE_CASES
            else f"{len(failures)} of {len(agent_rows)} scenarios failed; showing the first {MAX_FAILURE_CASES}."
        )
        if failures
        else "All genuinely-run scenarios passed their task-success check — no failures to analyze."
    )

    harness_note = (
        f"\n**{len(harness_rows)} of {len(rows)} scenarios could not be run** due to a harness/infra error "
        "(the simulated-customer or judge LLM call itself failed, e.g. a Gemini rate limit — not an agent "
        f"failure) and are excluded from every metric above: {', '.join(r['scenario_id'] for r in harness_rows)}.\n"
        if harness_rows
        else ""
    )

    return f"""# Agent Evaluation ({len(rows)} scenarios, {len(agent_rows)} genuinely run)

Generated by `scripts/run_agent_evals.py`. Agent brain: Groq (config.agent.brain_model).
Simulated customer: Gemini (config.agent.fallback_model). Judge: Groq llama-3.1-8b-instant
(a third, distinct configuration from both the brain and the simulated customer).
{harness_note}
## Aggregate metrics (over {len(agent_rows)} genuinely-run scenarios)

| Metric | Value |
|---|---|
| Task success rate | {overall_success:.0%} |
| Tool-call accuracy | {tool_accuracy:.0%} |
| Escalation correctness | {escalation_correctness:.0%} |
| Avg turns to resolution | {avg_turns:.2f} |
| Grounded-citation rate (kb_question only, n={len(kb_rows)}) | {citation_rate:.0%} |
| Avg reply-quality judge score | {avg_judge:.2f} / 5 |

## By category

| Category | N | Task success | Tool-call accuracy |
|---|---|---|---|
{chr(10).join(category_lines)}

## Per-scenario results

| Scenario | Category | Success | Turns | Judge |
|---|---|---|---|---|
{chr(10).join(detail_lines)}

## Failure analysis

{failure_note}

{chr(10).join(failure_lines) if failure_lines else ""}
"""


if __name__ == "__main__":
    rows = run_all_scenarios()
    report = build_report(rows)
    print(report)

    RESULTS_PATH.parent.mkdir(parents=True, exist_ok=True)
    RESULTS_PATH.write_text(report, encoding="utf-8")
    print(f"\nResults written to {RESULTS_PATH}")
